    public static void goToShabadPlayerActivity(Context context) {
        Intent intent = new Intent(context, ShabadPlayerActivity.class);
        Type token = new TypeToken<ArrayList<Shabad>>() {
        }.getType();
        Type tokenShabad = new TypeToken<Shabad>() {
        }.getType();

        ArrayList<Shabad> list = App.getGson().fromJson(App.getPrefranceData(MediaPlayerState.shabad_list), token);
        Shabad shabad = App.getGson().fromJson(App.getPrefranceData(MediaPlayerState.SHABAD), tokenShabad);
        long duration = App.getPreferenceLong(MediaPlayerState.SHABAD_DURATION);

        if (list != null && list.size() > 0) {
            intent.putExtra(PLAY_SONG, false);
            intent.putParcelableArrayListExtra("shabads", list);
            intent.putExtra("current_shabad", shabad);
            intent.putExtra(MediaPlayerState.SHABAD_DURATION, duration);
            context.startActivity(intent);
        }
    }

